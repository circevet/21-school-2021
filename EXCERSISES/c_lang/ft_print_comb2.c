#include <unistd.h>

void	ft_print_comb2(void)
{
	char	a;
	char	b;
	char	s;

	a = '0';
	s = ' ';
	while (a <= '9')
	{
		b = a;
		while (b <= '9')
		{			
			write(1, &a, 1);
			write(1, &b, 1);
			write(1, &s, 1);
			b++;
		}
		a++;
	}
}
